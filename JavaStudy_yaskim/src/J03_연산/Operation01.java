package J03_연산;

public class Operation01 {

	public static void main(String[] args) {
		int num = 10;
		num = num +1 ; 		// num++;
		//++num;
		System.out.println(++num); // 바로 1 증가 - 선증가
		System.out.println(num);
		System.out.println(num++);  // 증가 안됨(그 다음줄에 1증가함) - 후증가
		
		System.out.println(--num); // 선 감소
		System.out.println(num--); // 후 감소
		System.out.println(num);
		
		
		/*  관계연산자
		 *  == 두항이 같다(참)
		 *  != 두 항이 다르다(참)
		 * 
		 * 
		 * 
		 */
		
		
		
	}

}
